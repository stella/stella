const wordMimeTypes = [
  "application/msword",
  "application/vnd.openxmlformats-officedocument.wordprocessingml.document",
  "application/rtf",
  "application/vnd.oasis.opendocument.text",
];

const spreadsheetMimeTypes = [
  "application/vnd.ms-excel",
  "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
  "application/vnd.oasis.opendocument.spreadsheet",
  "text/csv",
];

const imageMimeTypes = ["image/jpeg", "image/png", "image/gif", "image/webp"];

export type DocumentIconProps = {
  mimeType: string;
  className?: string;
};

export function DocumentIcon({ mimeType, className }: DocumentIconProps) {
  if (mimeType === PDF_MIME_TYPE) {
    return <PdfIcon className={className} />;
  }

  if (wordMimeTypes.includes(mimeType)) {
    return <DocxIcon className={className} />;
  }

  if (spreadsheetMimeTypes.includes(mimeType)) {
    return <FileSpreadsheet className={className} />;
  }

  if (imageMimeTypes.includes(mimeType)) {
    return <FileImage className={className} />;
  }

  if (mimeType.startsWith("text/")) {
    return <FileText className={className} />;
  }

  return <File className={className} />;
}
