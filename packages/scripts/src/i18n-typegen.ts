#!/usr/bin/env bun
/**
 * Generates a TypeScript declaration file for the English
 * locale that preserves string literal types. This enables
 * use-intl to parse ICU variables at the type level and
 * enforce interpolation parameters.
 *
 * Usage: i18n-typegen <langs-dir>
 */
import { resolve } from "node:path";

const langsDir = process.argv[2];

if (!langsDir) {
  console.error("Usage: i18n-typegen <langs-dir>");
  process.exit(1);
}

const inputPath = resolve(langsDir, "en.json");
const outputPath = resolve(langsDir, "messages.gen.ts");

const json = await Bun.file(inputPath).text();
const messages: unknown = JSON.parse(json);

const indent = (depth: number) => "  ".repeat(depth);

let keyCount = 0;

const toLiteral = (value: unknown, depth: number): string => {
  if (typeof value === "string") {
    return JSON.stringify(value);
  }

  if (typeof value === "object" && value !== null && !Array.isArray(value)) {
    const entries = Object.entries(value);
    if (entries.length === 0) {
      return "Record<string, never>";
    }

    const lines = entries.map(([key, val]) => {
      keyCount++;
      return `${indent(depth + 1)}${JSON.stringify(key)}: ${toLiteral(val, depth + 1)};`;
    });
    return `{\n${lines.join("\n")}\n${indent(depth)}}`;
  }

  return "never";
};

const declaration = `// Auto-generated by generate-i18n-types.ts
// Do not edit manually.

type Messages = ${toLiteral(messages, 0)};

export default Messages;
`;

await Bun.write(outputPath, declaration);

console.log(`Generated ${outputPath} (${keyCount} keys)`);
